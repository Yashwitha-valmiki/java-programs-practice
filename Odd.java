public class Odd {
    public static void main(String[] args) {
        // to print numbers from 1 to 10
        for(int i = 1;i<=10;i++){
            System.out.print(i + " ");
        }
        // to print odd numbers from 1 to 20
        for(int i = 1;i<=20;i++){
            if(i%2!=0){
                System.out.println(i + " ");
            }
        }
    }
}
